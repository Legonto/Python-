# Задача 1
# Напишите программу, которая принимает на вход цифру, 
# обозначающую день недели, и проверяет, является ли 
# этот день выходным.
# Пример:
# - 6 -> да
# - 7 -> да
# - 1 -> нет

day = int(input("Введите день недели, проверим выходной ли это "))
if int(day) in range(1, 6):
    print(day, '-', 'нет')
elif int(day) in range(6, 8):
    print(day, '-', 'да')
else:
    print(day, '-', 'это не день недели')

